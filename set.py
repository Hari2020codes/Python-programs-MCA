set1={1,2,3,4}
set2={5,6,7,8}
print(set1)
print(set2)
#len
print(len(set1))
# min, max
print(min(set1))
print(max(set1))
#sum
print(sum(set1))

#sort
set3={6,3,5,2,6,7}
print(sorted(set3))

#union
print(set1.union(set2))
#intersection
print(set2.intersection(set3))
#difference
print(set2.difference(set3))